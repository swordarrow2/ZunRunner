package com.meng.zunRunner.anm;

public class Sprite {
    public int id;
    public float x;
    public float y;
    public float w;
    public float h;

    public Sprite(int id, float x, float y, float w, float h) {
        this.id = id;
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }
}
